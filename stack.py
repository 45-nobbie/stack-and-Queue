class stack:
    def __init__(self):
        stack.items = []
    def is_empty(self):
        return stack.items == []
    def push(self,item):
        self.items.append(item)
    def pop(self):
        return self.items.pop()
    def peek(self):
        return self.items[len(self.items)-1]
    def size(self):
        return len(self.items)
